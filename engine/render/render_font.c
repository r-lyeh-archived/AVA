font_t *fonts = 0;
int fonts_count = 0;

int font_mem( const void *fontData, int fileSize, int fontSize, int flags ) {
    static int seen = 0;
    if (!seen) {
        seen = 1;
        // init placeholder
        font_mem(bm_mini_ttf, bm_mini_ttf_length, 8, FONT_RANGE_EU /*512, no oversample*/ );
        // assign it to slot #0
        fonts[0] = fonts[1];
        fonts_count = 0;
    }

    // return placeholder font #0 (silkscreen) if null or empty data
    if(!fontData || fileSize <= 0 || fontSize <= 0) {
        return 0;
    }

    int id = (++fonts_count);
    fonts = (font_t*)realloc( fonts, sizeof(font_t) * (id+1) );
    font_t *f = &fonts[ id ];
    memset(f, 0, sizeof(font_t));

    // initProgram()
    const char* vs = 
        VS130
        "/* render_font.c */\n"
        "layout (location = 0) in vec3 att_position;\n"
        "layout (location = 1) in vec4 att_inColor0;\n"
        "layout (location = 2) in vec2 att_texCoord0;\n"
        "uniform mat4 u_worldMatrix;\n"
        "uniform mat4 u_viewProjMatrix;\n"
        "out vec4 v_color0;\n"
        "out vec2 v_uv0;\n"
        "void main() {\n"
        "    gl_Position = u_viewProjMatrix * u_worldMatrix * vec4( att_position.xyz, 1.0 );\n"
        "    v_color0 = att_inColor0;\n"
        "    v_uv0 = att_texCoord0;\n"
        "}\n";
    const char* fs = 
        FS130
        "/* render_font.c */\n"
        "uniform sampler2D u_mainTex;\n"
        "in vec4 v_color0;\n"
        "in vec2 v_uv0;\n"
        "void main() {\n"
        "    vec4 c = texture(u_mainTex, v_uv0);\n"
        "    fragColor = v_color0 * vec4(c.r, c.r, c.r, c.r);\n"
        "}\n";
    f->r.shader = shader(vs, fs);
    f->r.u_viewProjMatrix = glGetUniformLocation(f->r.shader, "u_viewProjMatrix");
    f->r.u_worldMatrix = glGetUniformLocation(f->r.shader, "u_worldMatrix");
    f->r.u_texture = glGetUniformLocation(f->r.shader, "u_mainTex");

    // font
    int atlasSize =
        flags & FONT_ATLAS_1024 ? 1024 :
        flags & FONT_ATLAS_2048 ? 2048 :
        flags & FONT_ATLAS_4096 ? 4096 : 512;

    f->size = fontSize; //16; // 20; // 44
    f->atlasWidth = atlasSize;
    f->atlasHeight = atlasSize;
    f->oversampleX = flags & FONT_OVERSAMPLE_X ? 2 : 1;
    f->oversampleY = flags & FONT_OVERSAMPLE_Y ? 2 : 1;

    int charCount = 1;
    for( int i = 0; unicode_ranges[i]; i += 3) {
        int lang = unicode_ranges[i];
        int begin = unicode_ranges[i+1];
        int end = unicode_ranges[i+2];
        if( flags & lang ) {
            charCount += 1 + (end - begin);
        }
    }
    charCount = 0xFFFF;
    
    f->lookup = (int*)calloc( 1, sizeof(int) * charCount );
    f->charInfo = malloc(sizeof(stbtt_packedchar) * charCount);
    uint8_t *atlasData = (uint8_t*)malloc(f->atlasWidth * f->atlasHeight);

    int total_index = 0;

    stbtt_pack_context context;
    if( !stbtt_PackBegin(&context, atlasData, f->atlasWidth, f->atlasHeight, 0, 1, (char*)0) ) {
        //panic("Failed to initialize atlas font");
    }
    for( int i = 0; unicode_ranges[i]; i += 3) {
        int begin = unicode_ranges[i+0];
        int end = unicode_ranges[i+1];
        int lang = unicode_ranges[i+2];
        if( flags & lang ) {
            LOGDEBUG(TEXT, "%x [%4x..%4x]", lang, begin, end);
            stbtt_PackSetOversampling(&context, f->oversampleX, f->oversampleY); /*useful on small fonts*/
            if( !stbtt_PackFontRange(&context, (unsigned char *)fontData, 0, f->size, begin, end - begin, (stbtt_packedchar*)f->charInfo + total_index) ) {
                /*panic("Failed to pack atlas font");*/
            }
            // unicode->index runtime lookup
            for( int i = begin; i <= end; ++i ) {
                f->lookup[ i ] = total_index + (i - begin);
            }
            total_index += end - begin;
        }
    }
    stbtt_PackEnd(&context);

    image img = {0};
    img.w = f->atlasWidth;
    img.h = f->atlasHeight;
    img.channels = 1;
    img.pixels = atlasData;
    f->r.texture = texture_mem(img, 0 /* |TEXTURE_NEAREST */ /* gl3: |TEXTURE_MIPMAPS */ /* gl2: |TEXTURE_ANISOTROPY*/);

    free(atlasData);

    // config character spacing
    uint32_t default_char = 'W'; // 'A'
    vec3 temp_vtx[4]; vec2 temp_uv[4];
    fillglyph( &temp_vtx[0], &temp_uv[0], f, default_char, &f->spaceX, &f->spaceY );
    if( f->spaceX > f->spaceY ) f->spaceY = f->spaceX;

    // add fixed kerning & interlining
    f->spaceX *= 1.25f;
    f->spaceY *= 1.50f;

    // note: if you create a 72-point font, the size of the font will be 96 pixels at 96 DPI, but 144 pixels at 144 DPI.
    // @todo: handle proper spacing below (we raster with pixel units, not dpi neither font sizes)
    float scaler = (fontSize * 1.5f) / 20;
    //f->spaceX *= scaler;
    //f->spaceY *= scaler;

    LOGDEBUG(FONT, "font spacing {%f,%f}", f->spaceX, f->spaceY);

    return id;
}

int font( const char *fontfile, int fontSize, int flags ) {
    char *data = file_read_(fontfile);
    int id = font_mem( data, file_size_(fontfile), fontSize, flags );
    free(data);
    return id;
}

void font_quit( int font_id ) {
    font_t *f = &fonts[ font_id ];

    renderer_destroy(&f->r);
    free( f->charInfo );
    free( f->lookup );

    font_t clear = {0};
    *f = clear;
}

